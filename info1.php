            <h3 style="color: red;"> Get stretched</h3>
            <p><span style="color: red;">-</span> Stretching can be a great way to relax, as it will connect your mind and body and keep your attention and concentration on the muscles and their work;</p>
            <h3 style="color: red;">Focus on yourself</h3>
            <p><span style="color: red;">-</span> One sure way to increase your level of self-confidence is not to pay attention to competitors and competitors around you, but to refresh yourself and tell yourself that you are the best and can not intimidate anything around you. Remember your coach's advice, focus on your path, and constantly think only about you and everything you've learned about swimming so far that can give you the edge at the crucial moment.</p>
            <ul>
              <p>-When you swim, all of the mental cues are focused on the front end of your stroke. This leads to an incredible amount of overthinking, slow movement, determination and tension in a part of the stroke at the front end where movement, softness, and flexibility are incredibly important to achieve good positioning. Whenever you try to acquire a new skill, ideally you progress from the slow-and-thoughtful to the fast-and-automated. With swimming, however, focusing thinking your way through the catch leads to paralysis by analysis! It slows you down and stiffens you up in entirely the wrong place!</p>
              <p>-Try filling a sink with some water and take a handful of water. 'Feel' it pass through your fingertips and back into the sink. This is 'feel' for the water. It is incredibly elusive and literally slips through your fingers. You will likely never feel anything. What you will feel is that moment when all the different components of your stroke 'dance' together in time. This is the feeling that you are trying to search for.</p>
              <p>-Firstly, and most importantly try relaxing. Forget about your catch, focus on keeping a relaxed lead arm and spend more of your time thinking about pushing back (working on that huge 85%). The real difference comes from getting the stroke to drive using a taut torso and hips, pressing back powerfully and keeping your hands in that 85% Propulsive Phase: not stiffly gliding, pausing and stabbing the water in front of your head.</p>
            </ul>
            <p>Watch a little bit of Strictly Come Dancing this week and apply the same grace to your arms in swimming, hold your posture perfect with your torso and ensure that your body leads what your arms are doing within your stroke. Don't let your arms control your body. Don't let the tail wag the dog...</p>
